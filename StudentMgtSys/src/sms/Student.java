package sms;

class Student
{
    String name;
    int rollNumber;
    double grade;
    int attendance;
    String address;
    long contact;

    public Student(String name, int rollNumber, double grade, int attendance, String address, long contact)
    {
        this.name = name;
        this.rollNumber = rollNumber;
        this.grade = grade;
        this.attendance = attendance;
        this.address = address;
        this.contact = contact;
    }
}